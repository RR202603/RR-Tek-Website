let currentLanguage = "zh";

function getTranslation(key, lang) {
    const keys = key.split(".");
    let value = languages[lang];

    for (const k of keys) {
        if (value && Object.prototype.hasOwnProperty.call(value, k)) {
            value = value[k];
        } else {
            console.warn(`Missing translation key: ${key} (${lang})`);
            return null;
        }
    }

    return value;
}

function updateContent() {
    document.documentElement.lang = currentLanguage === "zh" ? "zh-CN" : "en";

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.getAttribute("data-i18n");
        const translation = getTranslation(key, currentLanguage);

        if (translation !== null && translation !== undefined) {
            element.textContent = translation;
        }
    });

    updateBrandText();
}

function updateBrandText() {
    const brandText = document.getElementById("company-logo-text");
    if (brandText) {
        brandText.textContent = currentLanguage === "zh" ? "锐睿科技" : "RR-Tek";
    }
}

function toggleLanguage() {
    currentLanguage = currentLanguage === "zh" ? "en" : "zh";
    updateContent();
}

document.addEventListener("DOMContentLoaded", () => {
    updateContent();
});